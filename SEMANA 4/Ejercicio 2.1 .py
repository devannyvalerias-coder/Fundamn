print("\nEJERCICIO 2 con While - CUENTA REGRESIVA")

# Inicializamos el contador en 5
numero = 5

# Mientras el número sea mayor
while numero > 0:
    print(f"Faltan {numero} segundos...")
    numero = numero - 1 # Decrementamos el contador

print(" ¡Despegue! ")